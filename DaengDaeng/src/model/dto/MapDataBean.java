package model.dto;

public class MapDataBean {
	
	private int map_code;
	private String map_type;
	private double map_lat;
	private double map_lng;
	private String map_corp;
	private String map_address;
	private String map_contents;
	
	public int getMap_code() {
		return map_code;
	}
	public void setMap_code(int map_code) {
		this.map_code = map_code;
	}
	public String getMap_type() {
		return map_type;
	}
	public void setMap_type(String map_type) {
		this.map_type = map_type;
	}
	public double getMap_lat() {
		return map_lat;
	}
	public void setMap_lat(double map_lat) {
		this.map_lat = map_lat;
	}
	public double getMap_lng() {
		return map_lng;
	}
	public void setMap_lng(double map_lng) {
		this.map_lng = map_lng;
	}
	public String getMap_corp() {
		return map_corp;
	}
	public void setMap_corp(String map_corp) {
		this.map_corp = map_corp;
	}
	public String getMap_address() {
		return map_address;
	}
	public void setMap_address(String map_address) {
		this.map_address = map_address;
	}
	public String getMap_contents() {
		return map_contents;
	}
	public void setMap_contents(String map_contents) {
		this.map_contents = map_contents;
	}
	
	

}
