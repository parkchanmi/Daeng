package model.dto;

public class DogDataBean{
	private int dog_code;
	private int dog_memcode;
	private int file_code;
	private String dog_name;
	private String dog_gender;
	private String dog_age;
	private String dog_kind;
	private String dog_filepath;
	
	public int getDog_code() {
		return dog_code;
	}
	public void setDog_code(int dog_code) {
		this.dog_code = dog_code;
	}
	public int getDog_memcode() {
		return dog_memcode;
	}
	public void setDog_memcode(int dog_memcode) {
		this.dog_memcode = dog_memcode;
	}
	public int getFile_code() {
		return file_code;
	}
	public void setFile_code(int file_code) {
		this.file_code = file_code;
	}
	public String getDog_name() {
		return dog_name;
	}
	public void setDog_name(String dog_name) {
		this.dog_name = dog_name;
	}
	public String getDog_gender() {
		return dog_gender;
	}
	public void setDog_gender(String dog_gender) {
		this.dog_gender = dog_gender;
	}
	public String getDog_age() {
		return dog_age;
	}
	public void setDog_age(String dog_age) {
		this.dog_age = dog_age;
	}
	public String getDog_kind() {
		return dog_kind;
	}
	public void setDog_kind(String dog_kind) {
		this.dog_kind = dog_kind;
	}
	public String getDog_filepath() {
		return dog_filepath;
	}
	public void setDog_filepath(String dog_filepath) {
		this.dog_filepath = dog_filepath;
	}
	
	
}